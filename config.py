def get_credentials():
    """从环境变量读取天勤账号，缺失时给出清晰报错。"""
    user = os.environ.get("TQ_USER")
    pwd = os.environ.get("TQ_PWD")
    if not user or not pwd:
        raise RuntimeError(
            "未找到天勤账号凭证，请先设置环境变量：\n"
            "  export TQ_USER=你的用户名\n"
            "  export TQ_PWD=你的密码"
        )
    return user, pwd
